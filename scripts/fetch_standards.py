def main() -> int:
    # Windows 控制台默认 GBK，强制 UTF-8 输出
    if sys.stdout.encoding and sys.stdout.encoding.lower() != "utf-8":
        sys.stdout.reconfigure(encoding="utf-8")
    settings.standards_dir.mkdir(parents=True, exist_ok=True)
    for stage, content in STAGES.items():
        path = settings.standards_dir / f"stage_{stage}.md"
        path.write_text(content, encoding="utf-8")
        print(f"  [ok] {path.relative_to(settings.kb_dir.parent)}")
    print(f"\n完成：{len(STAGES)} 份课程标准节选已存入 {settings.standards_dir}")
    print("教师可手工编辑这些 .md 文件来调整内容。")
    return 0


if __name__ == "__main__":
    sys.exit(main())
